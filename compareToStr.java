// Compare two String using CompareTo()
public class compareToStr {
    public static void main(String args[]){
        String name1="Aman";
        String name2="Adil";
        if(name1.compareTo(name2)==0){
            System.out.println("String is equal ");
        }
        else{
            System.out.println("String is not equal ");
        }
    }
    
}
